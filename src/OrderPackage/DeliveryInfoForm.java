    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // vars
        String destination = cmbDestination.getSelectedItem().toString();
        String empID = cmbEmp.getSelectedItem().toString();

        // submit data to arraylist in orderForm
        deliveryInfo.put(destination, empID);

        // out
        JOptionPane.showMessageDialog(this, "Delivery Info submitted!",
                "Digi-Pizza | Best Pizzas for you!", JOptionPane.WARNING_MESSAGE);
        // test
        for (Map.Entry<String, String> entry : deliveryInfo.entrySet()) {
            System.out.println("Key: " + entry.getKey() + ", Value: " + entry.getValue());
        }
        this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

